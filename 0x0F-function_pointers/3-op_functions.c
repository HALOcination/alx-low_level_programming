#include "3-calc.h"

/**
 * op_add - calculats the sum of two integers
 * @a: the first int
 * @b: the second int
 *
 *Return: the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculats the difference of two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculats the division of two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculats the modulus of two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}

